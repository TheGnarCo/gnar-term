/**
 * jrvs Themes — included extension
 *
 * Ships four Jack-Kirby-inspired theme pairs (8 themes total). Each pair
 * shares a palette philosophy; dark + light variants of the same concept:
 *
 *   - jrvs cosmic         (general Kirby spot-color palette, void black)
 *   - jrvs cosmic light   (newsprint cream, CMYK ink)
 *   - jrvs boom tube      (warm focused orange glow on pitch black)
 *   - jrvs boom tube light (sun-bleached paper, warm accents)
 *   - jrvs highfather     (gilded gold + pale sky on starlight navy)
 *   - jrvs highfather light (chapel cream + burnished gold)
 *   - jrvs metron         (cold desaturated steel)
 *   - jrvs metron light   (paper-steel cool)
 *
 * Each theme registers via ExtensionAPI.registerTheme and shows up in
 * Settings › General › Theme alongside the built-in set.
 */
