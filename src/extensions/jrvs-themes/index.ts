// --- Registration ---

const THEMES: Array<[id: string, theme: ExtensionTheme]> = [
  ["jrvs-cosmic", jrvsCosmic],
  ["jrvs-cosmic-light", jrvsCosmicLight],
  ["jrvs-boom-tube", jrvsBoomTube],
  ["jrvs-boom-tube-light", jrvsBoomTubeLight],
  ["jrvs-highfather", jrvsHighfather],
  ["jrvs-highfather-light", jrvsHighfatherLight],
  ["jrvs-metron", jrvsMetron],
  ["jrvs-metron-light", jrvsMetronLight],
];

export function registerJrvsThemesExtension(api: ExtensionAPI): void {
  api.onActivate(() => {
    for (const [id, theme] of THEMES) {
      api.registerTheme(id, theme);
    }
  });
  // Cleanup happens automatically via REGISTRY_CLEANUP_FNS
  // (unregisterThemesBySource) when the extension deactivates.
}
